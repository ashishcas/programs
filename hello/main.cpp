#include<iostream>


using namespace std;
int main()
{
     // Variable Declaration
     int a;
     //Get Input Value
     cout<<"Enter the Number :";
     cin>>a;
     //If Condition Check
     if(a > 10)
     {
        // Block For Condition Success
        cout<<a<<" Is Greater than 10";
     }

     return 0;
 }
